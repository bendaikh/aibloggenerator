<script setup>
import { computed } from 'vue';
import { Head, Link, useForm, router, usePage } from '@inertiajs/vue3';
import SuperAdminLayout from '@/Layouts/SuperAdminLayout.vue';
import ImageUpload from '@/Components/ImageUpload.vue';

const page = usePage();

const props = defineProps({
    article: Object,
    currentWebsite: Object,
    websites: Array
});

const form = useForm({
    category_id: props.article.category_id || '',
    title: props.article.title || '',
    slug: props.article.slug || '',
    excerpt: props.article.excerpt || '',
    content: props.article.content || '',
    featured_image: props.article.featured_image || '',
    secondary_image: props.article.secondary_image || '',
    prep_time: props.article.prep_time || '',
    cook_time: props.article.cook_time || '',
    rest_time: props.article.rest_time || '',
    total_time: props.article.total_time || '',
    status: props.article.status || 'draft',
    published_at: props.article.published_at ? props.article.published_at.substring(0, 16) : ''
});

const submit = () => {
    form.put(route('superadmin.articles.update', { 
        website: props.currentWebsite?.id, 
        article: props.article.id 
    }));
};

const deleteArticle = () => {
    if (confirm(`Are you sure you want to delete "${props.article.title}"?`)) {
        router.delete(route('superadmin.articles.destroy', { 
            website: props.currentWebsite?.id, 
            article: props.article.id 
        }));
    }
};
</script>
